// Function to paraphrase the input text
function paraphraseText(inputText) {
    const words = inputText.split(' ');

    const paraphrasedWords = words.map(word => {
        const cleanWord = word.toLowerCase().replace(/[^\w\s]/gi, '');
        if (synonyms[cleanWord]) {
            const synonymList = [cleanWord, ...synonyms[cleanWord]];
            const randomIndex = Math.floor(Math.random() * synonymList.length);
            return word.replace(cleanWord, synonymList[randomIndex]);
        } else {
            return word;
        }
    });

    return paraphrasedWords.join(' ');
}
app.post('/paraphrase', (req, res) => {
    const inputText = req.body.inputText;

    // Check if inputText is provided
    if (!inputText) {
        return res.status(400).json({ error: 'Input text is required' });
    }

    // Paraphrase the input text
    const paraphrasedText = paraphraseText(inputText);

    // Send the paraphrased text in the response
    res.json({ paraphrasedText });
});
